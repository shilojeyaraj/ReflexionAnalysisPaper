"""
Reflection quality scoring using GPT-4o as judge.

Scores reflections on specificity, actionability, and accuracy (1-5 each).
The overall score is a weighted mean (actionability 0.5, specificity 0.3, accuracy 0.2).
"""

import json
import logging
from openai import OpenAI

logger = logging.getLogger(__name__)

JUDGE_SYSTEM_PROMPT = """You are evaluating the quality of a reflection generated by an AI agent after a task attempt.

Score each dimension from 1-5:
- specificity (1-5): Is the diagnosis concrete and specific (5) or vague and generic (1)?
- actionability (1-5): Would this reflection change the agent's behavior on the next attempt (5) or not (1)?
- accuracy (1-5): Does the diagnosis correctly identify what went wrong/right (5) or misdiagnose (1)?

Overall score formula: (actionability * 0.5) + (specificity * 0.3) + (accuracy * 0.2)

Respond with ONLY valid JSON, no other text:
{"specificity": <int>, "actionability": <int>, "accuracy": <int>, "overall": <float>, "reasoning": "<string>"}"""


def score_reflection(
    reflection: str,
    task: str,
    outcome: str,
    model: str = "gpt-4o",
) -> dict:
    """
    Score a single reflection using GPT-4o as judge.

    Args:
        reflection: The reflection text to score
        task: The task description
        outcome: What happened (feedback string or error_type)
        model: OpenAI model to use as judge

    Returns:
        dict with keys: specificity (int), actionability (int), accuracy (int),
                        overall (float), reasoning (str)
        On parse failure: {"error": str}
    """
    client = OpenAI()
    user_prompt = (
        f"Task: {task[:300]}\n\n"
        f"Outcome: {outcome[:200]}\n\n"
        f"Reflection to score:\n{reflection}"
    )
    try:
        response = client.chat.completions.create(
            model=model,
            messages=[
                {"role": "system", "content": JUDGE_SYSTEM_PROMPT},
                {"role": "user", "content": user_prompt},
            ],
            max_tokens=300,
            temperature=0.0,
        )
        content = response.choices[0].message.content or ""
        scores = json.loads(content)
        # Validate expected keys
        for key in ("specificity", "actionability", "accuracy", "overall"):
            if key not in scores:
                return {"error": f"Missing key '{key}' in judge response: {content}"}
        return scores
    except json.JSONDecodeError as e:
        return {"error": f"JSON parse failed: {e}"}
    except Exception as e:
        return {"error": str(e)}


def score_reflections_batch(
    reflections: list[dict],
    model: str = "gpt-4o",
) -> list[dict]:
    """
    Score a batch of reflection dicts in-place and return the list.

    Each input dict must have:
        reflection (str): The reflection text
        task_description (str): The task description
        error_type (str): The outcome/error category

    Adds 'reflection_quality' key to each dict with the score dict.

    Args:
        reflections: List of dicts (modified in-place)
        model: OpenAI model for judging

    Returns:
        The same list with 'reflection_quality' added to each dict
    """
    total = len(reflections)
    for i, item in enumerate(reflections):
        logger.info("Scoring reflection %d/%d ...", i + 1, total)
        score = score_reflection(
            reflection=item.get("reflection", ""),
            task=item.get("task_description", item.get("description", "")),
            outcome=item.get("error_type", ""),
            model=model,
        )
        item["reflection_quality"] = score
    return reflections
